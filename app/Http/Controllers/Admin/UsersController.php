    public function ban_user (User $user) {

        $blackListed = BlackList::where('user_id', $user->id)->first();

        if(!empty($blackListed)) {
            $blackListed->delete();
        } else {
            BlackList::create([
                'user_id' => $user->id,
                'reason' => 'Нарушение правил сайта'
            ]);
        }

        return back();
    }

